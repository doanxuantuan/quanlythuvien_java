    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (txtMaTaiLieu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Tài liệu !!!");
            check = false; 
            return;
        }
        btnChon.setEnabled(true);
        if (check) {
            TaiLieu tl = new TaiLieu();
            tl.setMaTaiLieu(txtMaTaiLieu.getText());
            tl.setTenTaiLieu(txtTenTaiLieu.getText());
            tl.setTacGia(txtTacGia.getText());
            tl.setSoLuong(Integer.valueOf(txtTongSo.getText()));
            tl.setConLai(Integer.valueOf(txtConLai.getText()));
            tl.setGiaBan(Integer.valueOf(txtGiaBan.getText()));
            tl.setAnh(ImagePath);
            
            boolean kq = TaiLieu_DAO.suaTaiLieu(tl);
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Cập nhật Tài liệu có mã: " + txtMaTaiLieu.getText() + " thành công!!!");
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Cập nhật Tài liệu Thất Bại !");
            btnSua.setText("Sửa");
            check = false;
        } else {
            int update = JOptionPane.showConfirmDialog(null, "Chỉnh sửa Tài liệu có mã: "+txtMaTaiLieu.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
            if (update == 0) {
                check = true;
                setEnable_True();
                btnSua.setText("Lưu");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

