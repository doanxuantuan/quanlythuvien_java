    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtTenTaiLieu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên Tài liệu !!!");
            txtTenTaiLieu.requestFocus();
            return;
        } else if (txtTacGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tác giả !!!");
            txtTacGia.requestFocus();
            return;
        }
        
        TaiLieu tl = new TaiLieu();
        tl.setMaTaiLieu(txtMaTaiLieu.getText());
        tl.setSoLuong(Integer.valueOf(txtTongSo.getText()));
        tl.setTenTaiLieu(txtTenTaiLieu.getText());
        tl.setTacGia(txtTacGia.getText());
        tl.setGiaBan(Integer.valueOf(txtGiaBan.getText()));
        tl.setConLai(Integer.valueOf(txtConLai.getText()));
        
        if(checkBtnMoi) {
            boolean kq = TaiLieu_DAO.themTaiLieu(tl);
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Thêm Tài liệu Thành công !!");
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Thêm Tài liệu Thất Bại");
            btnChon.setEnabled(false);
            checkBtnMoi = false;
        }
        else {
            if(checkx) {
                int sl = Integer.valueOf(txtTongSo.getText());
                boolean kq = false;
                try {
                    kq = TaiLieu_DAO.nhapTaiLieu(tl, sl);
                } catch (SQLException ex) {
                    Logger.getLogger(QL_TaiLieu.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (kq == true) {
                    JOptionPane.showMessageDialog(this, "Nhập Tài liệu Thành công !!");
                    setEnable_False();
                    hienThiDanhSach();
                }
                else JOptionPane.showMessageDialog(this, "Nhập Tài liệu Thất Bại"); 
                
                return;
            }
            int insert = JOptionPane.showConfirmDialog(null, "Nhập thêm Tài liệu có mã: "+txtMaTaiLieu.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
            if (insert == 0) {
                setEnable_True();
                txtTongSo.setText("");
                checkx=true;
                txtMaTaiLieu.setEnabled(true);
                txtMaTaiLieu.requestFocus();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnTKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKiemActionPerformed
        String str = txtTimKiem.getText();
        ArrayList<TaiLieu> ds = TaiLieu_DAO.layDSTaiLieuTheoTen(str);
        dtm.setRowCount(0);
        for(TaiLieu tl: ds) {
            Vector<Object> v = new Vector<Object>();
            v.add(tl.getMaTaiLieu());
            v.add(tl.getTenTaiLieu());
            v.add(tl.getTacGia());
            v.add(tl.getSoLuong());
            v.add(tl.getConLai());
            v.add(tl.getGiaBan());
            v.add(tl.getAnh());
            dtm.addRow(v);
        }
        tblDSTaiLieu.setModel(dtm);
    }//GEN-LAST:event_btnTKiemActionPerformed

