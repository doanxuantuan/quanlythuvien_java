    private void hienThiDanhSach() {
        ArrayList<TaiLieu> ds = TaiLieu_DAO.layDSTaiLieu();
        dtm.setRowCount(0);
        for(TaiLieu tl: ds) {
            Vector<Object> v = new Vector<Object>();
            v.add(tl.getMaTaiLieu());
            v.add(tl.getTenTaiLieu());
            v.add(tl.getTacGia());
            v.add(tl.getSoLuong());
            v.add(tl.getConLai());
            v.add(tl.getGiaBan());
            v.add(tl.getAnh());
            dtm.addRow(v);
        }
        tblDSTaiLieu.setModel(dtm);
    }
    
    public ImageIcon resizeImage(String ImagePath) {
        ImageIcon icon = new ImageIcon(ImagePath);
        Image img = icon.getImage();
        //Image newImg = img.getScaledInstance(lblAnhNV.getWidth(), lblAnhNV.getHeight(), Image.SCALE_SMOOTH);
        Image newImg = img.getScaledInstance(120, 140, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
    
