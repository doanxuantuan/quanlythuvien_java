    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String quyen = null;
        if (cboLoaiNV.getSelectedItem().equals("Quản trị")) {
            quyen = "AD";
        } else quyen = "TT";
        if(!NhanVien_DAO.checkTenDN(txtTenDN.getText())) {
            JOptionPane.showMessageDialog(this, "Đã tồn tại tài khoản: " + txtTenDN.getText() +" !!!");
            return;
        }
        if(txtHoTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên Nhân viên !!!");
            txtHoTen.requestFocus();
            return;
        } else if(txtTenDN.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên Đăng nhập !!!");
            txtTenDN.requestFocus();
            return;
        }
        
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        Date NgaySinh;
        try { 
            NgaySinh = Date.valueOf(txtNgaySinh.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập sai Ngày Sinh!! Định dạng: yyyy-MM-dd");
            txtNgaySinh.setText("");
            txtNgaySinh.requestFocus();
            return;
        }
        nv.setNgaySinh(NgaySinh);
        nv.setTenDN(txtTenDN.getText());
        nv.setSdt(txtSDT.getText());
        nv.setLoaiNV(quyen);
        nv.setAnh(ImagePath);
        boolean kq = NhanVien_DAO.themNhanVien(nv);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Thêm Nhân viên Thành công !!");
            btnChon.setEnabled(false);
            setEnable_False();
            hienThiDanhSach();
        }
        else JOptionPane.showMessageDialog(this, "Thêm Nhân viên Thất Bại");
    }//GEN-LAST:event_btnThemActionPerformed

