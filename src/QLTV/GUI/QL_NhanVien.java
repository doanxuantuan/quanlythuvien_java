    private void tblDSNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDSNhanVienMouseClicked
        int row = this.tblDSNhanVien.getSelectedRow();
        txtMaNV.setText((String) tblDSNhanVien.getValueAt(row, 0));
        txtHoTen.setText((String) tblDSNhanVien.getValueAt(row, 1));
        txtNgaySinh.setText((String) String.valueOf(tblDSNhanVien.getValueAt(row, 2)));
        txtTenDN.setText((String) tblDSNhanVien.getValueAt(row, 3));
        txtSDT.setText((String) String.valueOf(tblDSNhanVien.getValueAt(row, 5)));
        if (tblDSNhanVien.getValueAt(row, 4).equals("AD")) {
            cboLoaiNV.setSelectedIndex(0);
        } else cboLoaiNV.setSelectedIndex(1);
        ImagePath = (String) tblDSNhanVien.getValueAt(row, 6);
        lblAnhNV.setIcon(resizeImage(ImagePath));
    }//GEN-LAST:event_tblDSNhanVienMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtMaNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Nhân viên !!!");
            return;
        }
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setTenDN(txtTenDN.getText());
        int delete = JOptionPane.showConfirmDialog(null, "Chắc chắn muốn Xóa Nhân viên có mã: "+txtMaNV.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
        if (delete == 0) {
            boolean kq = NhanVien_DAO.xoaNhanVien(nv);
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Xóa Nhân viên Thành công !!");
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Xóa Nhân viên Thất Bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (txtMaNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Nhân viên !!!");
            check = false;
            return;
        }
        btnChon.setEnabled(true);
        String quyen = null;
        if (cboLoaiNV.getSelectedItem().equals("Quản trị")) {
            quyen = "AD";
        } else quyen = "TT";
        if (check) {
            NhanVien nv = new NhanVien();
            nv.setMaNV(txtMaNV.getText());
            nv.setHoTen(txtHoTen.getText());
            Date NgaySinh = null;
            try { 
                NgaySinh = Date.valueOf(txtNgaySinh.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Nhập sai Ngày Sinh!! Định dạng: yyyy-MM-dd");
                txtNgaySinh.setText("");
                txtNgaySinh.requestFocus();
                return;
            }
            nv.setNgaySinh(NgaySinh);
            nv.setTenDN(txtTenDN.getText());
            nv.setSdt(txtSDT.getText());
            nv.setLoaiNV(quyen);
            nv.setAnh(ImagePath);
            boolean kq = NhanVien_DAO.suaNhanVien(nv);
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Cập nhật Nhân viên có mã: " + txtMaNV.getText() + " thành công!!!");
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Cập nhật Nhân viên Thất Bại");
            btnSua.setText("Sửa");
            check = false;
        } else {
            int update = JOptionPane.showConfirmDialog(null, "Chỉnh sửa Nhân viên có mã: "+txtMaNV.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
            if (update == 0) {
                check = true;
                setEnable_True();
                btnSua.setText("Lưu");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoiActionPerformed
        setEnable_True();
        cboLoaiNV.setSelectedIndex(-1);
        txtSDT.setText("");
        txtNgaySinh.setText("");
        txtHoTen.setText("");
        txtTenDN.setText("");
        
        String str = "NV", strMa = "";
        int sl = tblDSNhanVien.getRowCount() - 1;
        strMa = (String) String.valueOf(tblDSNhanVien.getValueAt(sl, 0));
        strMa = strMa.substring(2, 4);
        int num = Integer. parseInt(strMa) + 1;
        if (num < 10)
           str = "NV0" + num;
        else str = "NV" + num;
        txtMaNV.setText(str);
        btnThem.setEnabled(true);
        btnChon.setEnabled(true);
        cboLoaiNV.setSelectedIndex(0);
    }//GEN-LAST:event_btnMoiActionPerformed

