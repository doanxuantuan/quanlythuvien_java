    private void txtSLMuonFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSLMuonFocusLost
        int sl = -1, slConLai = -1, checkINT = 1;
        slConLai = PhieuMuon_DAO.laySoLuongConLai(String.valueOf(cboMaTL.getSelectedItem()));
        try {
            sl = Integer.valueOf(txtSLMuon.getText());
            checkINT = 0;
        } catch (Exception e) {
            checkINT = 1;
            JOptionPane.showMessageDialog(this, "Vui lòng chỉ nhập số !!!");
        }
        if(checkINT == 0) {
            if(sl > 5) {
                check = false;
                flag = true;
                JOptionPane.showMessageDialog(this, "Số lượng mượn tối đa là 5 !!!");
            } else if(sl > slConLai) {
                check = false;
                flag = true;
                JOptionPane.showMessageDialog(this, "Tài liệu chỉ còn: "+slConLai+" bản in !!!");
            } else {
                check = true;
                flag = false;
            }
        }
    }//GEN-LAST:event_txtSLMuonFocusLost

