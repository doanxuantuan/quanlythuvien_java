    private void xuatPhieuMuon() {
        try {
            Hashtable map = new Hashtable();
            JasperReport rpt = JasperCompileManager.compileReport("src/QLTV/REPORT/rptPhieuMuon.jrxml");
            map.put("sMaPhieuMuon", txtMaPhieuMuon.getText());
            String connectionURL = PhieuMuon_DAO.getConnectionURL();
            System.out.println(connectionURL);
            Connection conn = DriverManager.getConnection(connectionURL);
            JasperPrint p = JasperFillManager.fillReport(rpt, map, conn);
            JasperViewer.viewReport(p, false);
        } catch (Exception e) { e.printStackTrace(); }
    }
    
    private void hienThiDanhSach() {
        ArrayList<PhieuMuon> ds = PhieuMuon_DAO.layDSPhieuMuon();
        dtm.setRowCount(0);
        for(PhieuMuon pm: ds) {
            Vector<Object> v = new Vector<Object>();
            v.add(pm.getMaPhieuMuon());
            v.add(pm.getMaPhieu());
            v.add(pm.getMaNVLap());
            v.add(pm.getMaDG());
            v.add(pm.getTinhTrang());
            v.add(pm.getNgayLap());
            dtm.addRow(v);
        }
        tblDSPhieuMuon.setModel(dtm);
    }
    
